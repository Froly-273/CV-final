def vis_from_txt(txtpath):
    with open(txtpath, 'r') as f:
        lines = f.readlines()
    img_buffer = {}
    for line in lines:
        line = line.strip('\n').split(" ")
        if line[0] not in img_buffer.keys():
            img_buffer[line[0]] = []
        img_buffer[line[0]].append([line[1], line[2], line[3], line[4], line[5]])  # bbox params

    for key in img_buffer.keys():
        val = img_buffer[key]
        img = 'datasets/VOCdevkit/VOC2007/JPEGImages/' + key + '.jpg'
        img = cv2.imread(img)
        for i in range(len(val)):
            x0 = int(float(val[i][1]))
            x1 = int(float(val[i][2]))
            y0 = int(float(val[i][3]))
            y1 = int(float(val[i][4]))
            score = float(val[i][0])
            if score < 0.5:
                continue
            color = (_COLORS[0] * 255).astype(np.uint8).tolist()
            text = '{}:{:.1f}%'.format("car", score * 100)
            txt_color = (0, 0, 0) if np.mean(_COLORS[0]) > 0.5 else (255, 255, 255)
            font = cv2.FONT_HERSHEY_SIMPLEX

            txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
            cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)
        cv2.imwrite('datasets/VOCdevkit/results/VOC2007/' + key + '.jpg', img)

